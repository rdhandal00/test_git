// creating a new array
let arr=[];

//adding an item to end of an array 
arr.push(7);

// logging array to console
console.log(arr);

// adding a value to the beggining of an array
arr.unshift(4);

//loging array to console
console.log(arr);

// adding one more value to end of an array
arr.push(3);

// removing first item from array
arr.shift();
// removing last item from array
arr.pop();

//logging array to console
console.log(arr);
